def get_icon(name: str) -> Image.Image | None:
    """
    Returns a 12×12 PIL Image (mode '1') for the given icon name.
    Black pixels = 0, white background = 255.
    Returns None if icon name is unknown.
    """
    data = _ICONS.get(name)
    if data is None:
        return None

    img = Image.new('1', (12, 12), 255)
    for y, row in enumerate(data):
        for x, pixel in enumerate(row):
            if pixel:
                img.putpixel((x, y), 0)
    return img
